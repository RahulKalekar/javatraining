package day3.Q5;

public class User {
    String name;
    String country;
    int age;
    String address;
    String email;
    String password;
    String mobile;

    public User(String name, String country, int age, String address, String email, String password, String mobile) {
        this.name = name;
        this.country = country;
        this.age = age;
        this.address = address;
        this.email = email;
        this.password = password;
        this.mobile = mobile;
    }
}